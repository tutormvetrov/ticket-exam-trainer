def main() -> int:
    if not DB_PATH.exists():
        print(f"DB not found: {DB_PATH}", file=sys.stderr)
        return 1

    con = sqlite3.connect(str(DB_PATH))
    con.row_factory = sqlite3.Row
    try:
        cur = con.cursor()
        cur.execute("SELECT title FROM tickets WHERE ticket_id = ?", (TICKET_ID,))
        row = cur.fetchone()
        if not row:
            print(f"Ticket not found: {TICKET_ID}", file=sys.stderr)
            return 2
        print(f"Ticket: {row['title']}")

        cur.execute(
            "SELECT block_code, length(expected_content) AS old_len "
            "FROM ticket_answer_blocks WHERE ticket_id = ?",
            (TICKET_ID,),
        )
        before = {r["block_code"]: r["old_len"] for r in cur.fetchall()}
        print(f"Existing blocks: {before}")

        for code, payload in NEW_BLOCKS.items():
            cur.execute(
                """
                UPDATE ticket_answer_blocks
                SET title = ?,
                    expected_content = ?,
                    confidence = 1.0,
                    is_missing = 0,
                    llm_assisted = 1
                WHERE ticket_id = ? AND block_code = ?
                """,
                (payload["title"], payload["content"], TICKET_ID, code),
            )
            if cur.rowcount == 0:
                cur.execute(
                    """
                    INSERT INTO ticket_answer_blocks
                        (ticket_id, block_code, title, expected_content,
                         confidence, is_missing, llm_assisted)
                    VALUES (?, ?, ?, ?, 1.0, 0, 1)
                    """,
                    (TICKET_ID, code, payload["title"], payload["content"]),
                )
            print(f"  {code}: {len(payload['content'])} chars (was {before.get(code, 0)})")

        cur.execute(
            "UPDATE tickets SET llm_status = 'claude-regen-pilot', llm_error = '' "
            "WHERE ticket_id = ?",
            (TICKET_ID,),
        )

        con.commit()
        print("OK — committed")
        return 0
    except Exception:
        con.rollback()
        raise
    finally:
        con.close()


if __name__ == "__main__":
    sys.exit(main())
