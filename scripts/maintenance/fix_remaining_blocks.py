def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Apply curated state-exam block repairs and refresh ticket canonical summaries.",
    )
    parser.add_argument(
        "--db",
        action="append",
        type=Path,
        help="SQLite database to update. Can be provided more than once. Defaults to the seed DB.",
    )
    parser.add_argument("--dry-run", action="store_true", help="Report changes without writing them.")
    return parser.parse_args()


def refresh_ticket_summaries(
    conn: sqlite3.Connection,
    ticket_ids: set[str],
    *,
    dry_run: bool,
) -> int:
    changed = 0
    for ticket_id in sorted(ticket_ids):
        ticket_row = conn.execute(
            "SELECT ticket_id, canonical_answer_summary FROM tickets WHERE ticket_id=?",
            (ticket_id,),
        ).fetchone()
        if not ticket_row:
            continue

        block_rows = conn.execute(
            """
            SELECT block_code, title, expected_content, is_missing
            FROM ticket_answer_blocks
            WHERE ticket_id=?
            ORDER BY block_code
            """,
            (ticket_id,),
        ).fetchall()
        blocks = [
            SimpleNamespace(
                block_code=row["block_code"],
                title=row["title"] or "",
                expected_content=normalize_reference_text(row["expected_content"] or ""),
                is_missing=bool(row["is_missing"]),
            )
            for row in block_rows
        ]
        new_summary = compose_reference_answer(
            SimpleNamespace(
                canonical_answer_summary=ticket_row["canonical_answer_summary"] or "",
                answer_blocks=blocks,
            )
        )
        if new_summary != (ticket_row["canonical_answer_summary"] or ""):
            changed += 1
            if not dry_run:
                conn.execute(
                    "UPDATE tickets SET canonical_answer_summary=? WHERE ticket_id=?",
                    (new_summary, ticket_id),
                )
    return changed


def apply_fixes(db_path: Path, fixes: dict[tuple[str, str], str], *, dry_run: bool = False) -> dict[str, int]:
    conn = sqlite3.connect(str(db_path))
    conn.row_factory = sqlite3.Row
    try:
        updated = 0
        unchanged = 0
        skipped = 0
        touched_ticket_ids: set[str] = set()
        for (ticket_id, block_code), new_content in fixes.items():
            row = conn.execute(
                """
                SELECT expected_content
                FROM ticket_answer_blocks
                WHERE ticket_id=? AND block_code=?
                """,
                (ticket_id, block_code),
            ).fetchone()
            if not row:
                print(f"  WARNING: no row matched ({ticket_id}, {block_code})")
                skipped += 1
                continue

            touched_ticket_ids.add(ticket_id)
            normalized_content = normalize_reference_text(new_content)
            if normalize_reference_text(row["expected_content"] or "") == normalized_content:
                unchanged += 1
                continue

            updated += 1
            if not dry_run:
                conn.execute(
                    """
                    UPDATE ticket_answer_blocks
                    SET expected_content=?
                    WHERE ticket_id=? AND block_code=?
                    """,
                    (normalized_content, ticket_id, block_code),
                )

        summaries = refresh_ticket_summaries(conn, touched_ticket_ids, dry_run=dry_run)
        if dry_run:
            conn.rollback()
        else:
            conn.commit()
        stats = {
            "updated": updated,
            "unchanged": unchanged,
            "skipped": skipped,
            "summaries": summaries,
        }
        print(
            "  Updated {updated} blocks, unchanged {unchanged}, skipped {skipped}, "
            "refreshed {summaries} summaries.".format(**stats)
        )
        return stats
    finally:
        conn.close()


def verify_fixes(db_path: Path, fixes: dict[tuple[str, str], str]) -> int:
    conn = sqlite3.connect(str(db_path))
    conn.row_factory = sqlite3.Row
    try:
        bad = 0
        for (ticket_id, block_code), expected in fixes.items():
            row = conn.execute(
                """
                SELECT expected_content
                FROM ticket_answer_blocks
                WHERE ticket_id=? AND block_code=?
                """,
                (ticket_id, block_code),
            ).fetchone()
            if not row:
                print(f"  MISSING: ({ticket_id}, {block_code})")
                bad += 1
                continue
            actual = row["expected_content"] or ""
            if normalize_reference_text(actual) != normalize_reference_text(expected):
                print(f"  MISMATCH: ({ticket_id}, {block_code})")
                bad += 1
    finally:
        conn.close()
    if bad == 0:
        print(f"  All {len(fixes)} blocks verified OK.")
    else:
        print(f"  {bad} blocks failed verification.")
    return bad


def main() -> int:
    args = parse_args()
    db_paths = args.db or [DEFAULT_DB]
    exit_code = 0
    for db_path in db_paths:
        db_path = db_path.resolve()
        print(f"\n=== {db_path} ===")
        if not db_path.exists():
            print("  ERROR: database does not exist.")
            exit_code = 1
            continue
        apply_fixes(db_path, FIXES, dry_run=args.dry_run)
        if verify_fixes(db_path, FIXES):
            exit_code = 1
    print("\nDone.")
    return exit_code


